use anyhow::{Context, Result};
use serde::Deserialize;
use std::collections::{HashMap, HashSet};
use std::path::{Path, PathBuf};

#[derive(Debug, Default, Deserialize)]
pub struct Config {
    /// Rust type → Python type overrides. Keys are Rust **path** spellings (e.g. `PyBbox`, `a::Foo`).
    /// Literal tuple types in source are not paths and cannot be keyed; use a `type` alias and map that name.
    /// Aliases listed here are preserved (not expanded to their definition) so nested types like `Vec<Alias>` work.
    #[serde(default)]
    pub type_map: HashMap<String, String>,

    #[serde(default)]
    pub features: FeaturesConfig,

    #[serde(default)]
    pub fallback: FallbackConfig,

    #[serde(default)]
    pub output: OutputConfig,

    /// Manual stub overrides for specific items
    #[serde(default, rename = "override")]
    pub overrides: Vec<OverrideEntry>,

    /// Extra Python snippets to splice into specific generated `.pyi` files (after generation, before write).
    #[serde(default, rename = "add_content")]
    pub add_content: Vec<AddContentEntry>,

    /// After generating .pyi files, run these format commands with generated .pyi paths appended.
    /// Each entry is a command string, e.g. "ruff format", "black", "ruff check --select I --fix".
    #[serde(default)]
    pub format: Vec<String>,
}

#[derive(Debug, Default, Deserialize)]
pub struct FeaturesConfig {
    /// cfg features considered active during parsing (used to evaluate #[cfg(feature = "...")]).
    #[serde(default)]
    pub enabled: Vec<String>,
}

#[derive(Debug, Deserialize)]
pub struct FallbackConfig {
    /// What to do when a type cannot be inferred statically:
    /// "any" (default) | "error" | "skip"
    #[serde(default = "default_fallback_strategy")]
    pub strategy: FallbackStrategy,
}

impl Default for FallbackConfig {
    fn default() -> Self {
        Self {
            strategy: FallbackStrategy::Any,
        }
    }
}

#[derive(Debug, Default, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "lowercase")]
pub enum FallbackStrategy {
    /// Emit `Any` and print a warning (default)
    #[default]
    Any,
    /// Hard error — stop generation and report the unknown type
    Error,
    /// Silently skip items with unresolvable types
    Skip,
}

fn default_fallback_strategy() -> FallbackStrategy {
    FallbackStrategy::Any
}

#[derive(Debug, Deserialize)]
pub struct OutputConfig {
    /// Python version string, affects `t.Optional[T]` vs `T | None` syntax (and related typing output).
    #[serde(default = "default_python_version")]
    pub python_version: String,

    /// Prepend "# Auto-generated by rylai" header
    #[serde(default = "default_true")]
    pub add_header: bool,
}

impl Default for OutputConfig {
    fn default() -> Self {
        Self {
            python_version: default_python_version(),
            add_header: true,
        }
    }
}

fn default_python_version() -> String {
    "3.10".to_string()
}

fn default_true() -> bool {
    true
}

#[derive(Debug, Clone, Deserialize)]
pub struct OverrideEntry {
    /// Fully-qualified item path, e.g. "my_module::complex_function"
    pub item: String,
    /// Python header line for a top-level `def` or `class` (single line, usually ending with `:`).
    /// Trailing `...` is optional and stripped; if the Rust item has no `///` doc, Rylai emits `...` as the body.
    /// Mutually exclusive with [`Self::param_types`] and [`Self::return_type`]; validated in [`Config::validate_overrides`].
    #[serde(default)]
    pub stub: Option<String>,
    /// Per-parameter Python type strings (full annotations after `:`) merged into the generated
    /// signature; keys are parameter names (`kwargs` and `**kwargs` normalize to the same key).
    /// Incompatible with [`Self::stub`]; may be combined with [`Self::return_type`].
    #[serde(default)]
    pub param_types: Option<HashMap<String, String>>,
    /// Full Python return type string for the generated `def` line (replaces Rust-inferred return).
    /// Incompatible with [`Self::stub`]; may be combined with [`Self::param_types`].
    #[serde(default)]
    pub return_type: Option<String>,
}

/// Normalize a `param_types` config key so `kwargs`, `*args`, and `**kwargs` spellings map to the same bare name.
pub fn normalize_param_types_key(key: &str) -> String {
    let s = key.trim();
    if let Some(rest) = s.strip_prefix("**") {
        return rest.trim().to_string();
    }
    if let Some(rest) = s.strip_prefix('*') {
        return rest.trim().to_string();
    }
    s.to_string()
}

/// Where to insert [`AddContentEntry::content`] in a generated `.pyi` file.
#[derive(Debug, Clone, Copy, Deserialize, PartialEq, Eq)]
#[serde(rename_all = "kebab-case")]
pub enum AddContentLocation {
    /// Immediately after the `# Auto-generated by rylai...` banner (if present); otherwise at the start of the file.
    Head,
    /// After the first line `import typing as t`.
    AfterImportTyping,
    /// At end of file.
    Tail,
}

/// Inject raw Python into a generated stub file identified by path relative to `-o`.
#[derive(Debug, Deserialize)]
pub struct AddContentEntry {
    /// Relative path using `/`, e.g. `abcd.pyi` or `pkg/sub.pyi` (must match output layout).
    pub file: String,
    pub location: AddContentLocation,
    /// Raw Python. A trailing newline is appended automatically when missing so you need not end with `\n`.
    pub content: String,
}

/// Normalize `file` from config: relative path only, no `..`, optional `.pyi` suffix.
pub fn normalize_add_content_file(s: &str) -> anyhow::Result<PathBuf> {
    use anyhow::bail;
    let s = s.trim();
    if s.is_empty() {
        bail!("add_content.file must not be empty");
    }
    if s.starts_with('/') || s.contains('\\') {
        bail!("add_content.file must be a relative path using '/': {}", s);
    }
    let mut out = PathBuf::new();
    for seg in s.split('/') {
        if seg.is_empty() || seg == "." {
            continue;
        }
        if seg == ".." {
            bail!("add_content.file must not contain '..': {}", s);
        }
        out.push(seg);
    }
    if out.as_os_str().is_empty() {
        bail!("add_content.file is not a valid path: {}", s);
    }
    if out.extension().and_then(|e| e.to_str()) != Some("pyi") {
        out.set_extension("pyi");
    }
    Ok(out)
}

/// Compare two relative stub paths from layout vs config.
pub fn canonical_stub_rel_path(p: &Path) -> PathBuf {
    p.components().collect()
}

/// Every `add_content` entry must refer to a `.pyi` that is generated in this run.
pub fn validate_add_content_targets(
    entries: &[AddContentEntry],
    generated_rel_paths: &[PathBuf],
) -> anyhow::Result<()> {
    use anyhow::bail;
    if entries.is_empty() {
        return Ok(());
    }
    let set: HashSet<PathBuf> = generated_rel_paths
        .iter()
        .map(|p| canonical_stub_rel_path(p))
        .collect();
    for e in entries {
        let p = normalize_add_content_file(&e.file)?;
        let key = canonical_stub_rel_path(&p);
        if !set.contains(&key) {
            bail!(
                "add_content: no stub generated for file = {:?} (check -o and package layout)",
                e.file
            );
        }
    }
    Ok(())
}

/// Version-specific rendering decisions, computed once from the configured Python version
/// and threaded through all generation code.
///
/// Adding a new version-specific behavior means adding a field here and deriving it in
/// `from_version` — no changes needed elsewhere in the generation pipeline.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct RenderPolicy {
    /// Emit `T | None` (py ≥ 3.10) instead of `t.Optional[T]` (py < 3.10).
    pub union_optional: bool,
    /// Emit `t.Self` (py ≥ 3.11, PEP 673) instead of the class name in stubs (`import typing as t`).
    /// When `false`, class names are used as forward references and
    /// `from __future__ import annotations` is added to the output.
    pub native_self: bool,
    /// Prepend `from __future__ import annotations` to the generated file.
    /// Needed when using class names as forward references (i.e. when `!native_self`).
    pub future_annotations: bool,
    /// Use PEP 585 style `list[T]`, `dict[K, V]`, `tuple[...]`, `set[T]` (py ≥ 3.9).
    /// When `false` (e.g. Python 3.8), emit `t.List`, `t.Dict`, `t.Tuple`, `t.Set` instead.
    pub pep585_builtin_generics: bool,
}

impl RenderPolicy {
    pub fn from_version(major: u32, minor: u32) -> Self {
        let v = (major, minor);
        let native_self = v >= (3, 11);
        Self {
            union_optional: v >= (3, 10),
            native_self,
            // forward-reference class names require lazy annotation evaluation
            future_annotations: !native_self,
            pep585_builtin_generics: v >= (3, 9),
        }
    }
}

/// Deep-merge two TOML values; keys in `override_val` take precedence over `base`.
/// Array tables (e.g. `[[override]]`) are replaced as a whole, not merged item-by-item.
fn deep_merge_toml(base: toml::Value, override_val: &toml::Value) -> toml::Value {
    use toml::Value;
    match (base, override_val) {
        (Value::Table(mut base_t), Value::Table(override_t)) => {
            for (k, ov) in override_t {
                let merged = base_t
                    .remove(k.as_str())
                    .map(|b| deep_merge_toml(b, ov))
                    .unwrap_or_else(|| ov.clone());
                base_t.insert(k.clone(), merged);
            }
            Value::Table(base_t)
        }
        (_, override_v) => override_v.clone(),
    }
}

/// Read `[tool.rylai]` from pyproject.toml if present.
fn tool_rylai_from_pyproject(path: &Path) -> Option<toml::Value> {
    let content = std::fs::read_to_string(path).ok()?;
    let doc: toml::Value = content.parse().ok()?;
    doc.get("tool").and_then(|t| t.get("rylai")).cloned()
}

impl Config {
    /// Load config by merging `[tool.rylai]` from pyproject.toml (base) and rylai.toml (override).
    /// When both exist, duplicate keys are resolved in favor of rylai.toml; otherwise both apply.
    pub fn load_merged(rylai_toml_path: &Path, pyproject_path: &Path) -> Result<Self> {
        let base = tool_rylai_from_pyproject(pyproject_path)
            .unwrap_or_else(|| toml::Value::Table(Default::default()));
        let override_val = if rylai_toml_path.exists() {
            let content = std::fs::read_to_string(rylai_toml_path)?;
            toml::from_str(&content).context("parse rylai.toml")?
        } else {
            toml::Value::Table(Default::default())
        };
        let merged = deep_merge_toml(base, &override_val);
        let config: Self = merged
            .try_into()
            .map_err(|e| anyhow::anyhow!("invalid config structure: {}", e))?;
        config.validate_overrides()?;
        Ok(config)
    }

    /// `[[override]]`: use `stub` alone, **or** at least one of non-empty `param_types` / `return_type`.
    /// `stub` must not appear together with `param_types` or `return_type`.
    pub fn validate_overrides(&self) -> Result<()> {
        use anyhow::bail;
        for (i, o) in self.overrides.iter().enumerate() {
            let has_stub = o.stub.as_ref().is_some_and(|s| !s.trim().is_empty());
            let has_pt = o.param_types.as_ref().is_some_and(|m| !m.is_empty());
            let has_rt = o.return_type.as_ref().is_some_and(|s| !s.trim().is_empty());

            if has_stub && (has_pt || has_rt) {
                bail!(
                    "[[override]] must not combine `stub` with `param_types` or `return_type` (entry index {i})"
                );
            }
            if !has_stub && !has_pt && !has_rt {
                bail!(
                    "[[override]] must set `stub`, or non-empty `param_types`, or non-empty `return_type` (entry index {i})"
                );
            }

            if has_pt {
                let mut seen: HashMap<String, String> = HashMap::new();
                for k in o.param_types.as_ref().expect("has_pt").keys() {
                    let n = normalize_param_types_key(k);
                    if let Some(first) = seen.get(&n) {
                        bail!(
                            "[[override]] param_types: duplicate keys after normalization (`{k}` and `{first}` → `{n}`) at entry index {i}"
                        );
                    }
                    seen.insert(n, k.clone());
                }
            }
        }
        Ok(())
    }

    /// Return the Python version as (major, minor) tuple for comparison.
    pub fn python_version_tuple(&self) -> (u32, u32) {
        let parts: Vec<u32> = self
            .output
            .python_version
            .split('.')
            .filter_map(|s| s.parse().ok())
            .collect();
        (
            parts.first().copied().unwrap_or(3),
            parts.get(1).copied().unwrap_or(10),
        )
    }

    /// Compute the rendering policy for the configured Python version.
    pub fn render_policy(&self) -> RenderPolicy {
        let (major, minor) = self.python_version_tuple();
        RenderPolicy::from_version(major, minor)
    }
}

/// Rust idents for which every `[type_map]` key sharing this last segment maps to the same Python type.
/// Used when expanding `type` aliases: only then is the alias name kept for [`crate::type_map::map_type`]
/// (e.g. `Vec<PyBbox>` stays tied to `PyBbox` instead of expanding to a tuple first).
pub fn type_map_preserve_alias_idents(type_map: &HashMap<String, String>) -> HashSet<String> {
    let mut by_last: HashMap<String, HashSet<String>> = HashMap::new();
    for (k, v) in type_map {
        let last = k.rsplit("::").next().unwrap().to_string();
        by_last.entry(last).or_default().insert(v.clone());
    }
    by_last
        .into_iter()
        .filter(|(_, vs)| vs.len() == 1)
        .map(|(ident, _)| ident)
        .collect()
}

/// Merge `[type_map]` entries into the pyclass-derived `known_classes` map used during stub generation.
/// Inserts full Rust path keys first (deterministic order), then short-name entries only when the
/// Python target is unambiguous for that last segment. Warns and keeps existing mappings on conflict.
pub(crate) fn merge_type_map_into_known_classes(
    known_classes: &HashMap<String, String>,
    type_map: &HashMap<String, String>,
) -> (HashMap<String, String>, Vec<String>) {
    let mut warnings = Vec::new();
    let mut m = known_classes.clone();

    let mut keys: Vec<String> = type_map.keys().cloned().collect();
    keys.sort();

    for k in &keys {
        let v = type_map.get(k).expect("key from sorted list");
        match m.get(k.as_str()) {
            None => {
                m.insert(k.clone(), v.clone());
            }
            Some(existing) if existing == v => {}
            Some(existing) => {
                warnings.push(format!(
                    "rylai: [type_map] key `{k}` conflicts with existing Rust→Python mapping (`{existing}` vs `{v}`); keeping existing"
                ));
            }
        }
    }

    let mut last_groups: HashMap<String, Vec<(String, String)>> = HashMap::new();
    for k in &keys {
        let v = type_map.get(k).expect("key from sorted list");
        let last = k.rsplit("::").next().unwrap().to_string();
        last_groups
            .entry(last)
            .or_default()
            .push((k.clone(), v.clone()));
    }

    let mut lasts: Vec<String> = last_groups.keys().cloned().collect();
    lasts.sort();

    for last in lasts {
        let entries = last_groups.get(&last).expect("last segment group");
        let distinct: HashSet<&str> = entries.iter().map(|(_, v)| v.as_str()).collect();
        if distinct.len() > 1 {
            let detail: Vec<String> = entries.iter().map(|(k, v)| format!("{k} → {v}")).collect();
            warnings.push(format!(
                "rylai: [type_map] ambiguous type name `{last}` (multiple Python targets): {}; not registering short name — use full Rust paths in config or disambiguate",
                detail.join(", ")
            ));
            continue;
        }
        let v = &entries[0].1;
        match m.get(&last) {
            None => {
                m.insert(last.clone(), v.clone());
            }
            Some(existing) if existing == v => {}
            Some(existing) => {
                warnings.push(format!(
                    "rylai: [type_map] short name `{last}` would map to `{v}` but existing mapping is `{existing}`; keeping existing"
                ));
            }
        }
    }

    (m, warnings)
}

